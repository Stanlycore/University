# Задача 3: Дана длина ребра куба. Найти объём куба и площадь его боковой поверхности.
__author__ = "Непомнящих Станислав"
import sys
from t3_modul import volume, side_surface_area


def print_help() -> None:
    """
    Выводит справочную информацию о программе.
    """
    help_text = (
        "Использование: python t3.py [длина ребра куба]\n"
        "Если параметр не указан, программа запросит ввод с клавиатуры.\n"
        "Этот скрипт вычисляет объём куба и площадь его боковой поверхности."
    )
    print(help_text)


def main() -> None:
    if len(sys.argv) > 1 and sys.argv[1] in ("-h", "--help"):
        print_help()
        return

    if len(sys.argv) > 1:
        try:
            a = float(sys.argv[1])
        except ValueError:
            print("Неверный формат аргумента. Ожидается число.")
            sys.exit(1)
    else:
        a = float(input("Введите длину ребра куба: "))

    print("Объём куба:", volume(a))
    print("Площадь его боковой поверхности:", side_surface_area(a))


# Объяснение:
# Конструкция 'if __name__ == "__main__": main()' гарантирует, что функция main() будет вызвана только тогда,
# когда скрипт запускается напрямую. Если модуль импортирован, код внутри этого блока не выполнится.
if __name__ == '__main__':
    main()
